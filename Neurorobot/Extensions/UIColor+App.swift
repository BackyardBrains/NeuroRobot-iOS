import UIKit

extension UIColor {
    @nonobjc class var appGreen: UIColor {
        return UIColor(r: 0, g: 172, b: 0)
    }
    
    @nonobjc class var appBeige: UIColor {
        return UIColor(r: 255, g: 230, b: 204)
    }
    
    @nonobjc class var appPurple: UIColor {
        return UIColor(r: 230, g: 179, b: 255)
    }
    
    @nonobjc class var appYellow: UIColor {
        return UIColor(r: 230, g: 153, b: 77)
    }
}
